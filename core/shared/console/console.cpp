#include "core/shared/console/console.h"

namespace core
{
	void CONSOLE::write(const std::string& in_line)
	{
		if (!in_line.empty() && is_present())
		{
			const std::string final_line = in_line + "\n";
			CONSOLE_PLATFORM::write(final_line);
		}
	}
}